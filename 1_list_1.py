# EXAMPLE OF PYTHON LIST AND SOME OPERATIONS [1]

destination = ['Bhavnagar', 'Rajkot', 'Surat', 'Ahmedabad', 'Gandhinagar', 'Jamnagar'] # Creating a list


print(destination)  # Printing all values


print(destination[0]) # Printing 1 value


print(destination[0:5]) #Printing first 5 values


print(destination * 2)  #Printing list twice


print(destination[1:5]) #Printing 2 to 5 values  
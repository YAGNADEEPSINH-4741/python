result = num1 != num2  
print(f"{result} = {num1} != {num2}")

result = num1 < num2  
print(f"{result} = {num1} < {num2}")

result = num1 > num2 
print(f"{result} = {num1} > {num2}")

result = num1 <= num2
print(f"{result} = {num1} <= {num2}")

result = num1 >= num2  
print(f"{result} = {num1} >= {num2}")